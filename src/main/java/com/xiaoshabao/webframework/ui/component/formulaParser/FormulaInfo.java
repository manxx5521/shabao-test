package com.xiaoshabao.webframework.ui.component.formulaParser;

public class FormulaInfo {
	private String formulaId;
	private String formulaExp;
	private String formulaContent;

	public String getFormulaId() {
		return formulaId;
	}

	public void setFormulaId(String formulaId) {
		this.formulaId = formulaId;
	}

	public String getFormulaExp() {
		return formulaExp;
	}

	public void setFormulaExp(String formulaExp) {
		this.formulaExp = formulaExp;
	}

	public String getFormulaContent() {
		return formulaContent;
	}

	public void setFormulaContent(String formulaContent) {
		this.formulaContent = formulaContent;
	}

}

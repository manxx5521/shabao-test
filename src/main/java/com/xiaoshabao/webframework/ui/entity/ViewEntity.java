package com.xiaoshabao.webframework.ui.entity;

public class ViewEntity {
	private String viewId;
	private String viewName;
	private String billId;
	private Integer viewPosition;
	private Integer viewType;
	private String viewExtId;
	private Integer orderNo;
	public String getViewId() {
		return viewId;
	}
	public void setViewId(String viewId) {
		this.viewId = viewId;
	}
	public String getViewName() {
		return viewName;
	}
	public void setViewName(String viewName) {
		this.viewName = viewName;
	}
	public String getBillId() {
		return billId;
	}
	public void setBillId(String billId) {
		this.billId = billId;
	}
	public Integer getViewPosition() {
		return viewPosition;
	}
	public void setViewPosition(Integer viewPosition) {
		this.viewPosition = viewPosition;
	}
	public Integer getViewType() {
		return viewType;
	}
	public void setViewType(Integer viewType) {
		this.viewType = viewType;
	}
	public String getViewExtId() {
		return viewExtId;
	}
	public void setViewExtId(String viewExtId) {
		this.viewExtId = viewExtId;
	}
	public Integer getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(Integer orderNo) {
		this.orderNo = orderNo;
	}
	
}

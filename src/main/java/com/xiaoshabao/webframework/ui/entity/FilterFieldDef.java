package com.xiaoshabao.webframework.ui.entity;
/**
 * 字段过滤器
 */
public class FilterFieldDef {
private String filedname;
private String sqlfieldname;
/**
 * @return the filedname
 */
public String getFiledname() {
	return filedname;
}
/**
 * @param filedname the filedname to set
 */
public void setFiledname(String filedname) {
	this.filedname = filedname;
}
/**
 * @return the sqlfieldname
 */
public String getSqlfieldname() {
	return sqlfieldname;
}
/**
 * @param sqlfieldname the sqlfieldname to set
 */
public void setSqlfieldname(String sqlfieldname) {
	this.sqlfieldname = sqlfieldname;
}

}

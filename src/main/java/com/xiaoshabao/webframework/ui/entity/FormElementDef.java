package com.xiaoshabao.webframework.ui.entity;
/**
 * 表单元素定义
 */
public class FormElementDef {
	protected Long id;
	protected String type;
	protected String catecory;
	protected String name;
	protected String param;
	protected String label;
	protected String help;
	protected String tempplate;
	protected String viewtempplate;
	
   

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type
	 *            the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * @return the catecory
	 */
	public String getCatecory() {
		return catecory;
	}

	/**
	 * @param catecory
	 *            the catecory to set
	 */
	public void setCatecory(String catecory) {
		this.catecory = catecory;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the param
	 */
	public String getParam() {
		return param;
	}

	/**
	 * @param param
	 *            the param to set
	 */
	public void setParam(String param) {
		this.param = param;
	}

	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * @param label
	 *            the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * @return the help
	 */
	public String getHelp() {
		return help;
	}

	/**
	 * @param help
	 *            the help to set
	 */
	public void setHelp(String help) {
		this.help = help;
	}

	/**
	 * @return the tempplate
	 */
	public String getTempplate() {
		return tempplate;
	}

	/**
	 * @param tempplate
	 *            the tempplate to set
	 */
	public void setTempplate(String tempplate) {
		this.tempplate = tempplate;
	}

	/**
	 * @return the viewtempplate
	 */
	public String getViewtempplate() {
		return viewtempplate;
	}

	/**
	 * @param viewtempplate
	 *            the viewtempplate to set
	 */
	public void setViewtempplate(String viewtempplate) {
		this.viewtempplate = viewtempplate;
	}

}
